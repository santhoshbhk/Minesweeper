package minesweeper;

public class customPair {
	private int i, j;
	
	customPair(int inputi, int inputj)
	{
		i = inputi;
		j = inputj;
	}

	public int geti() {
		return i;
	}

	public void seti(int inputi) {
		this.i = inputi;
	}

	public int getj() {
		return j;
	}

	public void setj(int inputj) {
		this.j = inputj;
	}
	
	
}
